/**
 * Get compatible moves for a Pokemon
 * @param {number} pokemonId - The ID of the Pokemon
 * @returns {Array} - Array of compatible move names
 */
function getCompatibleMoves(pokemonId) {
    return moveCompatibility[pokemonId] || [];
}

/**
 * Check if a Pokemon can learn a move
 * @param {number} pokemonId - The ID of the Pokemon
 * @param {string} moveName - The name of the move
 * @returns {boolean} - True if Pokemon can learn the move
 */
function canLearnMove(pokemonId, moveName) {
    const compatibleMoves = moveCompatibility[pokemonId] || [];
    return compatibleMoves.includes(moveName);
}
